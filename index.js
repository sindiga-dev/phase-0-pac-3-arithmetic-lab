//function add 
function add(a, b){
    return a+b;
}
//subtract
function subtract(a, b){
    return a-b;
}
//function multiply
function multiply(a, b){
    return a*b;
}
//function divide
function divide(a, b){
    return a/b;
}
//function increment
function increment(n){
    return n+1;
}

function decrement(n){
    return n-1;
}

function makeInt(n) {
    let a = parseInt(n, 10);
    return a;
  }
  
  // floating number return
  
  function preserveDecimal(n) {
    let a = parseFloat(n);
    return a;
  }